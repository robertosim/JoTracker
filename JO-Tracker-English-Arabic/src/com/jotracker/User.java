package com.jotracker;


public class User {
	private String Login, Id, Name, Imei, Device_number, User_number, Expiry_date, Car_number, Date, License_number, License_expiration;

	public String getLogin() {
		return Login;
	}

	public void setLogin(String login) {
		Login = login;
	}

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getImei() {
		return Imei;
	}

	public void setImei(String imei) {
		Imei = imei;
	}

	public String getDevice_number() {
		return Device_number;
	}

	public void setDevice_number(String device_number) {
		Device_number = device_number;
	}

	public String getUser_number() {
		return User_number;
	}

	public void setUser_number(String user_number) {
		User_number = user_number;
	}

	public String getExpiry_date() {
		return Expiry_date;
	}

	public void setExpiry_date(String expiry_date) {
		Expiry_date = expiry_date;
	}

	public String getCar_number() {
		return Car_number;
	}

	public void setCar_number(String car_number) {
		Car_number = car_number;
	}

	public String getDate() {
		return Date;
	}

	public void setDate(String date) {
		Date = date;
	}

	public String getLicense_number() {
		return License_number;
	}

	public void setLicense_number(String license_number) {
		License_number = license_number;
	}

	public String getLicense_expiration() {
		return License_expiration;
	}

	public void setLicense_expiration(String license_expiration) {
		License_expiration = license_expiration;
	}

}